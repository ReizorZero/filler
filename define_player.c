#include "filler.h"

void	define_player(t_filler *f, int fd)
{
	char *s;

	get_next_line(fd, &s);
	f->t = (s[10] == '2') ? -2 : -1;
	f->et = (f->t == -2) ? -1 : -2;
	ft_strdel(&s);
}
